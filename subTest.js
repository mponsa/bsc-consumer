const getTransactionsForToken = async (tokenAddress) => {
  const tokenContract = new web3.eth.Contract(ABI_BEP_20, tokenAddress);
  const symbol = (await tokenContract.methods.symbol().call({})).toUpperCase()

  console.log(`Starting transctions fetch for ${symbol}`)


  tokenContract.events
    .Transfer({})
    .on("connected", function (subscriptionId) {
      console.log(`Connected! subscriptionId: ${subscriptionId}`);
    })
    .on("data", async function (event) {
      //console.log(`Found a Tcake transaction txHash: ${event.transactionHash}`);
      
      
      var receipt = await web3.eth.getTransactionReceipt(event.transactionHash);
      if (
        receipt.to &&
        receipt.to.toUpperCase() !==
        "0x05ff2b0db69458a0750badebc4f9e13add608c7f".toUpperCase()
      )
      return;
      
      var tx = await web3.eth.getTransaction(event.transactionHash);
      const call = abiDecoder.decodeMethod(tx.input);

      if (call && checkSwapTx(call.name)){
        if(validatePath(call)){
          const [from, to] = getPathFromTo(call)
          const logs = abiDecoder.decodeLogs(receipt.logs);
          const tokenTransfers = logs.filter((x) => x.name === "Transfer");
          const fromEvent = tokenTransfers.filter(event => event.address.toUpperCase() === from.toUpperCase())[0]
          const toEvent = tokenTransfers.filter(event => event.address.toUpperCase() === to.toUpperCase())[0]

          if (isBuy(to, tokenAddress)) {
            printTransaction(fromEvent, toEvent, "\x1b[32m", "BUY", event.transactionHash);
          } else {
            printTransaction(toEvent, fromEvent, "\x1b[31m", "SELL", event.transactionHash);
          }
        }
      }
    });
};

const checkSwapTx = (fn) => {
  return ['swapExactTokensForTokens','swapTokensForExactTokens','swapExactETHForTokens','swapTokensForExactETH','swapExactTokensForETH','swapETHForExactTokens'].indexOf(fn) >= 0
} 

const validatePath = (call) => {
  //Get only tx envolving BUSD or TCAKE as initial or final destiny
  const addresses = call.params.filter(param => param.name == 'path')[0].value
  if([TCAKE, BUSD].indexOf(addresses[0]) >= 0 && [TCAKE, BUSD].indexOf(addresses[addresses.length - 1]) >= 0){
    return true
  }

  return false
}

const getPathFromTo = (call) => {
  const addresses = call.params.filter(param => param.name == 'path')[0].value
  return [addresses[0],addresses[addresses.length - 1]]
}

const isBuy = (to, tokenAddress) => {
  return to.toUpperCase() === tokenAddress.toUpperCase();
};

